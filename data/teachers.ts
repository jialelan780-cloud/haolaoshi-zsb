// 隐私说明：学员案例统一以「姓氏 + 同学」匿名展示；录取数据仅用作汇总，不公开个人姓名。

export interface StatItem {
  label: string;
  value: string;
  unit?: string;
}

export interface MathModule {
  title: string;
  desc: string;
}

export interface TimelineItem {
  year: string;
  content: string;
}

export interface CaseResult {
  mathScore?: string; // 数学最终分数
  totalScore?: string; // 总分
  admission?: string; // 上岸学校 / 方向
  improvement?: string; // 提升幅度 / 亮点
}

export interface StudentCase {
  id: string;
  title: string; // 案例标题（保留原文完整标题）
  studentName: string; // 匿名代称，如「周同学」
  tags: string[]; // 学生标签：基础薄弱 / 高分突破 / 深夜提问 ...
  initialSituation: string; // 学生起点（2-3 句）
  mainProblems: string[]; // 主要问题
  teachingProcess: string[]; // 真实带教过程，多段长文本（故事主体）
  turningPoint: string; // 关键转折
  result: CaseResult; // 最终结果
  teacherReflection: string; // 老师寄语 / 案例总结
}

export interface SpecialCase {
  title: string;
  desc: string;
}

export interface ClassInfo {
  name: string; // 班级名称
  stage: string; // 授课阶段
  time: string; // 上课时间
  status: "进行中" | "已满班" | "即将开班"; // 当前状态
  suitableFor: string; // 适合学生类型
}

export interface Testimonial {
  content: string;
  author: string; // 匿名，如「2024 届学员」「一位家长」
}

export interface TeachingStyle {
  features: string[]; // 讲课特点
  suitableForWeak: boolean; // 是否适合基础薄弱学生
  focusFormula: boolean; // 是否注重公式理解
  focusPatterns: boolean; // 是否注重题型归纳
  focusExamSkills: boolean; // 是否注重应试技巧
  hasReview: boolean; // 是否有课后答疑和错题复盘经验
  description: string; // 整体风格描述
}

export interface Teacher {
  id: string;
  name: string;
  /** 头像用姓氏首字 + 渐变底色生成，无需图片资源 */
  avatarText: string;
  avatarFrom: string;
  avatarTo: string;
  subject: string; // 主授科目
  teachingYears: string; // 教龄
  totalStudents: string; // 累计带教学生数
  expertise: string[]; // 擅长方向
  oneLineIntro: string; // 一句话介绍
  currentStatus: string; // 当前带班状态

  intro: string[]; // 自我介绍，按段落

  teachingBelief: string; // 带教理念 / 一句励志寄语

  experience: {
    cohorts: string; // 教过多少届
    studentTypes: string[]; // 熟悉哪些学生类型
    goodAtModules: string[]; // 擅长哪些模块
    classTypes: string[]; // 负责过哪些班型
  };

  teachingStyle: TeachingStyle;

  mathModules: MathModule[];

  timeline: TimelineItem[];

  studentCases: StudentCase[];

  stats: StatItem[];

  admissionSchools: string[]; // 代表性录取院校（来自录取情况表，仅展示校名）

  specialCases: SpecialCase[];

  schedule: ClassInfo[];

  studentTestimonials: Testimonial[];

  parentTestimonials: Testimonial[];

  feedback: StatItem[]; // 课堂反馈情况
}

